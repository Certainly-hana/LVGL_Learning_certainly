//IO方向设置
#define max_SDA_IN()  {GPIOF->MODER&=~(3<<(0*2));GPIOF->MODER|=0<<0*2;}	//PB9输入模式
#define max_SDA_OUT() {GPIOF->MODER&=~(3<<(0*2));GPIOF->MODER|=1<<0*2;} //PB9输出模式
//IO操作函数	 
#define max_IIC_SCL    PFout(1) //SCL
#define max_IIC_SDA    PFout(0) //SDA	 
#define max_READ_SDA   PFin(0)  //输入SDA 

